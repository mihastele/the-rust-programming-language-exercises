pub fn processing_a_series_of_items_with_iterators() {
    let v1 = vec![1, 2, 3];
    let v1_iter = v1.iter();

    for val in v1_iter {
        println!("Got: {val}");
    }

    let v1: Vec<i32> = vec![1, 2, 3];

    // In order to use the iterator, we need to consume it using collect()
    let v2: Vec<_> = v1.iter().map(|x| x + 1).collect();

    assert_eq!(v2, vec![2, 3, 4]);

    let v1: Vec<i32> = vec![1, 2, 3];

    // Map only creates the iterator, but does not consume it. We need to consume it manually using collect()
    let v2: Vec<_> = v1.iter().map(|x| x + 1).collect();

    assert_eq!(v2, vec![2, 3, 4]);

}

